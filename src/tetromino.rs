// In Tetris, the pieces are officially called tetrominoes — shapes made of four connected squares.
// They are traditionally named by letters that resemble their shapes:
//
// Here are the seven standard Tetris pieces:
// I-tetromino - A straight line of 4 blocks (sometimes cyan).
// O-tetromino - A 2×2 square (yellow).
// T-tetromino - A T-shaped piece (purple).
// S-tetromino - A zigzag that leans right (green).
// Z-tetromino - A zigzag that leans left (red).
// J-tetromino - An L-shape with the “short” block on the left (blue).
// L-tetromino - An L-shape with the “short” block on the right (orange).
//
// So the “names” of the Tetris figures are: I, O, T, S, Z, J, L tetrominoes.
